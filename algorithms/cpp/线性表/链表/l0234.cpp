/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     ListNode *next;
 *     ListNode() : val(0), next(nullptr) {}
 *     ListNode(int x) : val(x), next(nullptr) {}
 *     ListNode(int x, ListNode *next) : val(x), next(next) {}
 * };
 */
class Solution {
public:
    bool isPalindrome(ListNode *head) {
        stack<int> s;
        ListNode *p = head;
        int count = 0;
        while (p) {
            s.push(p->val);
            p = p->next;
            count++;
        }
        p = head;
        for (int i = 0; i < count / 2; i++) {
            if (p->val != s.top()) {
                return false;
            }
            p = p->next;
            s.pop();
        }
        return true;
    }
};
